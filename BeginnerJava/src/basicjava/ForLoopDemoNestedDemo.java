
package basicjava;


public class ForLoopDemoNestedDemo {
    public static void main(String[] args) {
        
        for(int i=0;i<5;i++){
            for(int j=0;j<5;j++){
                System.out.println(j);
                            
            }
            System.out.println("Break");
        }
        System.out.println("-------------");
        
        for(int k=0;k<10;k++){
            for(int l=0;l<10;l++){
                System.out.println("hello world!!!");
            }
            System.out.println("------------");
        }
        
        
        
    }
    
}
